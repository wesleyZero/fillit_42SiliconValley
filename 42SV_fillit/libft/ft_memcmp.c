#include <string.h>

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			i;
	unsigned char	*str1;
	unsigned char	*str2;
	int				diff;

	i = 0;
	diff = 0;
	str1 = (unsigned char*)s1;
	str2 = (unsigned char*)s2;
	while (i < n)
	{
		if (str1[i] != str2[i])
		{
			diff = (int)str1[i] - str2[i];
			return (diff);
		}
		i++;
	}
	return (diff);
}
