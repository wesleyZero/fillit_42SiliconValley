#include <unistd.h>

void		ft_putnbr(int num)
{
	char			c;

	c = '0';
	if (num == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (num < 0)
	{
		write(1, "-", 1);
		num *= -1;
	}
	if (num >= 10)
	{
		c = num % 10 + '0';
		ft_putnbr(num / 10);
		write(1, &c, 1);
	}
	else
	{
		c = num % 10 + '0';
		write(1, &c, 1);
		return ;
	}
}
